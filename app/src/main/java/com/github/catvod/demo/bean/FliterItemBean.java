package com.github.catvod.demo.bean;

public class FliterItemBean {
    public String name;
    public String valve;
    public String categeName;
    public String categeKey;

    public String getCategeName() {
        return categeName;
    }

    public void setCategeName(String categeName) {
        this.categeName = categeName;
    }

    public String getCategeKey() {
        return categeKey;
    }

    public void setCategeKey(String categeKey) {
        this.categeKey = categeKey;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValve() {
        return valve;
    }

    public void setValve(String valve) {
        this.valve = valve;
    }
}
